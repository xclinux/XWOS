#include <xwos/standard.h>
#include <xwos/mm/mempool/allocator.h>

/******** ******** ******** ******** ******** ******** ******** ********
 ******** ******** ********       macros      ******** ******** ********
 ******** ******** ******** ******** ******** ******** ******** ********/

/******** ******** ******** ******** ******** ******** ******** ********
 ******** ********         function prototypes         ******** ********
 ******** ******** ******** ******** ******** ******** ******** ********/

/******** ******** ******** ******** ******** ******** ******** ********
 ******** ******** ********       .data       ******** ******** ********
 ******** ******** ******** ******** ******** ******** ******** ********/
extern struct xwmm_mempool * eram_mempool;

/******** ******** ******** ******** ******** ******** ******** ********
 ******** ********      function implementations       ******** ********
 ******** ******** ******** ******** ******** ******** ******** ********/
void * xwlua_alloc(void * ud, void * ptr, size_t osize, size_t nsize)
{
        void * mem;

        XWOS_UNUSED(ud);

        if (NULL == ptr) {
                xwmm_mempool_malloc(eram_mempool, nsize, &mem);
        } else if (0 == nsize) {
                xwmm_mempool_free(eram_mempool, ptr);
                mem = NULL;
        } else if (osize >= nsize) {
                mem = ptr;
        } else {
                mem = ptr;
                xwmm_mempool_realloc(eram_mempool, nsize, &mem);
        }
        return mem;
}
